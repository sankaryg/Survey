package com.example.survey_game_fuction;

public class ServerData {

	private int id;
	private String contra_Feature;
	private String comp_Feature;
	private int feature_Id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getContra_Feature() {
		return contra_Feature;
	}
	public void setContra_Feature(String contra_Feature) {
		this.contra_Feature = contra_Feature;
	}
	public String getComp_Feature() {
		return comp_Feature;
	}
	public void setComp_Feature(String comp_Feature) {
		this.comp_Feature = comp_Feature;
	}
	public int getFeature_Id() {
		return feature_Id;
	}
	public void setFeature_Id(int feature_Id) {
		this.feature_Id = feature_Id;
	}
	
	
}
